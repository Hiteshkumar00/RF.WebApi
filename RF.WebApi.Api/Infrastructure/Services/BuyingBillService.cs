        /// <summary>
        /// Syncs the expense entries from a buying bill into the BusinessExpence table.
        /// Existing linked expenses are updated/removed; new ones are added.
        /// </summary>
        private async Task SyncBillExpencesToBusinessExpences(
            int billId, int accountId, DateOnly billDate,
            List<BuyingBillExpenceSyncDto> incomingExpences)
        {
            // Load all existing BusinessExpences linked to this bill
            var existing = await _context.BusinessExpences
                .Include(e => e.Payments)
                .Where(e => e.BuyingBillId == billId && e.AccountId == accountId)
                .ToListAsync();

            var incomingIds = incomingExpences
                .Where(e => e.Id.HasValue)
                .Select(e => e.Id!.Value)
                .ToHashSet();

            // Remove expenses that are no longer in the list
            foreach (var exp in existing.Where(e => !incomingIds.Contains(e.Id!.Value)))
            {
                _context.BusinessExpencePayments.RemoveRange(exp.Payments);
                _context.BusinessExpences.Remove(exp);
            }

            foreach (var incoming in incomingExpences)
            {
                if (incoming.Id.HasValue && incoming.Id.Value > 0)
                {
                    // Update existing
                    var exp = existing.FirstOrDefault(e => e.Id == incoming.Id.Value);
                    if (exp != null)
                    {
                        exp.ExpenceType = incoming.ExpenceType;
                        exp.TotalAmount = incoming.TotalAmount;
                        exp.Date = billDate;

                        // Sync payments
                        var incomingPayments = incoming.Payments.Select(p => new BusinessExpencePayment
                        {
                            Id = p.Id ?? 0,
                            Amount = p.Amount,
                            PaymentAccountId = p.PaymentAccountId,
                            BusinessExpenceId = exp.Id ?? 0,
                            Date = p.Date ?? billDate
                        }).ToList();

                        _context.SyncCollection(exp.Payments, incomingPayments, (e, d) => d.Id > 0 && e.Id == d.Id, _mapper);
                    }
                }
                else
                {
                    // Create new
                    var newExp = new BusinessExpence
                    {
                        AccountId = accountId,
                        BuyingBillId = billId,
                        ExpenceType = incoming.ExpenceType,
                        TotalAmount = incoming.TotalAmount,
                        Date = billDate
                    };
                    
                    foreach (var p in incoming.Payments)
                    {
                        newExp.Payments.Add(new BusinessExpencePayment
                        {
                            Amount = p.Amount,
                            PaymentAccountId = p.PaymentAccountId,
                            Date = p.Date ?? billDate
                        });
                    }
                    _context.BusinessExpences.Add(newExp);
                }
            }

            await _context.SaveChangesAsync();
        }

        private sealed class BuyingBillExpenceSyncDto
        {
            public int? Id { get; set; }
            public string ExpenceType { get; set; } = string.Empty;
            public decimal TotalAmount { get; set; }
            public List<BuyingBillExpencePaymentDto> Payments { get; set; } = new();
        }
    }
